// 注文手続きページ「配送希望日」プルダウンの項目作成（ページ読み込み時）
// 選択肢の親要素（ul）にid名`deliveryDateOptions`をつける
$(function () {
	const startDayCount = 3; // 当日の何日後の選択肢から作成するか
	const endDayCount = 14;	// 何日分の選択肢を作成するか

	let now = new Date();
	now.setDate(now.getDate() + startDayCount);
	const youbiArray = ["日", "月", "火", "水", "木", "金", "土"];

	let year;
	let month;
	let day;
	let youbi;

	let html = '';

	html += '<li class="c-pulldown_form-value-items_content">';
	html += `<input class="c-pulldown_form-value_input" type="radio" name="designated_date" id="form4-0" value="指定なし" onchange="c_pulldownCheckedForm4()" checked>`;
	html += `<label class="c-pulldown_form-value_label" for="form4-0">`;
	html += '指定なし';
	html += '</label>';
	html += '</li>';
	
	for (let i = 1; i < endDayCount; i++) {
		year = now.getFullYear();
		month = now.getMonth() + 1;
		day = now.getDate();
		youbi = youbiArray[now.getDay()];

		val = year + "/" + month + "/" + day + "（" + youbi + "）";

		html += '<li class="c-pulldown_form-value-items_content">';
		html += `<input class="c-pulldown_form-value_input" type="radio" name="designated_date" id="form4-${i}" value="${val}" onchange="c_pulldownCheckedForm4()">`;
		html += `<label class="c-pulldown_form-value_label" for="form4-${i}">`;
		html += val;
		html += '</label>';
		html += '</li>';

		now.setDate(now.getDate() + 1);
	}
	$('#deliveryDateOptions').html(html);
});


